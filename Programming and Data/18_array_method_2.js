let colors = ["빨간색", "주황색", "노란색", "초록색", "파란색", "보라색"];

console.log(colors);

// 배열의 첫 요소를 삭제: shift()
colors.shift();
console.log(colors);

// 배열의 마지막 요소를 삭제: pop()
colors.pop();
console.log(colors);

// 배열의 첫 요소로 값 추가: unshift(value)
colors.unshift("검은색");
console.log(colors);

// 배열의 마지막 요소로 값 추가: push()
colors.push("흰색");
console.log(colors);
